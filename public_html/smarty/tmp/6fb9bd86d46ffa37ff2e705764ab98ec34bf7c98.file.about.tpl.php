				<h2>What We Do</h2>

				<p><strong>Purpose:</strong> Fungi Growing on Wood is a web
				site devoted to the identification and appreciation of fungi
				growing in close association with living and dead wood. The
				fungi described here are those in a parasitic or saprophytic
				relationship with trees. Mycorrhizal fungi, also in close
				association with trees, are not strongly represented here even
				though some of them may be found on well-rotted wood. The
				organisms described on this web site are often referred to as
				lignicolous fungi. Most species are illustrated with multiple
				images to show as many diagnostic details as possible. To my
				knowledge, no guide similar to this exists.</p>

				<p><strong>Area of Usefulness:</strong> The keys were developed
				for fungi in the Northeastern United States. As with all
				regional keys, coverage decreases toward the periphery of the
				specified range. Many of the species included in these pages
				are distributed much more widely than the indicated area.</p>

				<p><strong>Approach to Identification:</strong> Identification
				of lignicolous fungi at this site requires the use of
				dichotomous keys based on macroscopic characters. By
				restricting the web site to lignicolous fungi, many, many other
				fungi are excluded. By excluding terrestrial fungi and fungi
				restricted to other substrates, a much smaller subset of
				organisms is defined. Consequently, the identification process
				is simplified.</p>

				<p><strong>Importance of Fungi on Wood:</strong> Lignicolous
				fungi are important. As a group, they play a vital role in
				recycling the nutrients locked up in wood. Economically, some
				species are capable of killing or damaging valuable timber and
				landscape trees. From a hobbyist perspective, no matter what
				time of year or the weather, it is almost always possible to
				find fungi on wood and they have saved many a foray from coming
				up empty handed. Some are edible and highly prized. Many are
				beautiful, serving as subjects for photographers and artists.
				All of them are useful in teaching aspects of mycology.</p>

				<div class="collapse">
					<p><strong>More Information:</strong> If you are unfamiliar
					with fungi and using keys, be sure to review the
					information on keys, the glossary, and some of the
					particulars about the species included in these keys.</p>

					<h2>Why Wood Decay Fungi?</h2>

					<p>Fungi which grow on wood are sometimes called
					"lignicolous" fungi. But why develop a set of keys limited
					to fungi utilizing wood as a substrate? After all, being
					lignicolous does not define a taxonomic category.
					Lignicolous fungi include ascomycetes and basidiomycetes
					and a large number of classes and orders within each of
					these groups. Most of these taxa include both lignicolous
					and terrestrial species. Rather than taxonomy, the keys
					focus on the biological activity holding this otherwise
					disparate group of fungi together: their ability to degrade
					cellulose and lignin, the major components of wood. This
					ability is often judged negatively on our part. In fact,
					the introduction to Illustrated Genera of Wood Decay Fungi
					by Dr. Fergus states that "This manual has therefore been
					prepared with the hope that it will fill a definite need,
					that of the general forester to identify decay fungi. It
					will also provide an illustrated Key for use in a Forest
					Pathology Laboratory course." Dr. Fergus indicates that the
					keys in his manual were based on those used by Dr. L.O.
					Overholts for use in a course in Forest Pathology. Dr.
					Fergus' book begins to answer the question posed earlier.
					One reason you might want a set of keys to wood decay fungi
					is because these fungi cause economic loss. Forest trees
					and valuable landscape trees can be infected and rotted by
					these fungi. Knowing the species growing on a tree can help
					the forester determine the likely extent of loss. Different
					species are associated with different amounts of decay in
					the tree. Additionally, some species are restricted to
					sapwood and will not affect the merchantable volume of
					heartwood. Some fungi can decay sound wood, others decay
					only decaying wood and bark.</p>

					<p>But being able to identify lignicolous fungi causing
					economic loss is only one reason why keys to lignicolous
					fungi might be useful. In the material below, I briefly
					describe other important reasons people have to know the
					species of fungi which are found growing on wood. Wood
					decay fungi are the preeminant recyclers of wood in
					ecosystems. Without these fungi, wood would never decay. We
					would be "up to our eyeballs" in twigs, limbs, and tree
					trunks. Worse, the valuable nutrients in this wood would be
					locked up and unavailable for new growth. The species of
					fungi responsible for decaying the wood of the different
					species of hardwood and softwood trees is of ecological
					interest. Wood decay fungi include many sought-after edible
					species such as <i>Pleurotus ostreatus</i>, <i>Grifola
					frondosa</i>, and <i>Laetiporus sulphureus</i>.</p>

					<ul>
						<li>Wood decay fungi are used as mycomedicinals.
						Preparations made from species such as <em>Ganoderma
						lucidum</em> and <em>Trametes versicolor</em> are the
						mycological equivalent of herbal medicinals.</li>

						<li>Wood decay fungi are screened for pharmaceutical
						and industrial compounds.</li>

						<li>Wood decay fungi are favorite subjects for
						photographers and other artists.</li>

						<li>Wood decay fungi are used by hobbyists to dye wool
						and other fabrics. <em>Trametes suaveolens</em>, for
						example, yields a much sought purple dye.</li>

						<li>Wood decay fungi are used by hobbyists to make
						paper. Many of the species contain tough, fibrous cells
						which, when separated, can be fashioned into ornamental
						paper products.</li>

						<li>Wood decay fungi utilize different proportions of
						cellulose and lignin from wood, leading to what is
						termed white rot or brown rot. This character is
						sometimes given taxonomic importance. For example, most
						species now in the genus <em>Oligoporus</em> were
						formally included in the genus <em>Tyromyces</em>.
						<em>Tyromyces</em>, however, is now restricted to white
						rot species and any brown rot species formally in it
						were transferred to the genus <em>Oligoporus</em> which
						is restricted to brown rot species. For example,
						<em>Oligoporus caesius</em> was formally <em>Tyromyces
						caesius</em>.</li>

						<li>Wood decay fungi provide fascinating examples of
						biological relationships. For example, <em>Armillaria
						mellea</em> is a much sought edible species. It is also
						one of the few gilled mushrooms that is an important
						forest tree parasite. <em>Armillaria mellea</em> is
						sometimes parasitized by another gilled mushroom called
						<em>Entoloma abortivum</em>, or the Aborted Entoloma. A
						parasitized <em>Armillaria</em> becomes a more or less
						solid mass of flesh and never develops gills. The two
						species often grow near each other and for many years
						it was thought that the aborted form was
						<em>Entoloma</em> parasitized by <em>Armillaria</em>.
						It was only in recent years that the reverse was shown
						to be true. To call <em>Entoloma abortivum</em>, the
						Aborted Entoloma, now makes little sense but will the
						name change?</li>

						<li>Wood decay fungi provide subjects for student
						research projects. For example, students at Messiah
						College have worked with me to study relationships of
						wood decay fungi to plant species. This type of study
						attempts to determine which wood decay fungi occur most
						commonly on wood of a given species of tree. This sort
						of study not only requires the identification of the
						fungus but also the identification of the wood, a
						formidable task at times given that most of these fungi
						are found on dead logs and stumps in various states of
						decay.</li>
					</ul>

					<p>A set of keys to fungi restricted to growing on wood
					eases the identification process for people with all of the
					above interests. Field guides and technical monographs
					cover all genera and species of a group. The identification
					process can become tedious for groups with many terrestrial
					species or when using comprehensive field guides.
					Furthermore, technical literature is often not available to
					the general user as it may reside in obscure locations or
					it may require more technical mycological "know how" than
					that possessed by the general user.</p>

					<p>For any of the activities described above, it is my hope
					that these keys and pictures, within their limitations,
					will be found useful to identify many of the fungi found
					growing on wood.</p>

					<h2>Species Included in the Keys</h2>

					<p>A number of criteria were used to decide which species
					to include in the keys:</p>

					<p><strong>First</strong> - the fungus must display
					macroscopic characters (features visible with the unaided
					eye or with a 10x loupe) that are sufficiently distinctive
					to allow identification with reasonable certainty. Species
					with obscure features, or which can only be identified with
					the use of a microscope, are not included.</p>

					<p><strong>Second -</strong> preference was given to common
					species. I used cumulative species lists of Western Penn
					Mushroomers, Eastern Penn Mushroomers, New Jersey
					Mycological Association, field guides, my own collecting
					experience, and observations at NEMF and NAMA meetings to
					help determine which species to include. Although the
					emphasis is on common species, the keys do include some
					less commonly encountered species.</p>

					<p><strong>Third -</strong> the organism must utilize wood
					as a substrate. There is a degree of latitude with this
					criterion. Some species will be found only on living trees
					or on relatively intact dead wood. Others occur on wood
					that is quite decomposed (well-rotted) and just a few steps
					away from becoming soil. Similarly, I included a number of
					species which regularly appear on landscape bark mulch or
					similar substrates. Finally, some species are included
					which are typically described as terrestrial (on soil) but
					which may also at times occur on well-rotted wood. Keep in
					mind that certain species may appear terrestrial but in
					reality are attached to buried wood (e.g., roots) or to a
					tree trunk at the soil line.</p>
					
					<p><strong>Fourth -</strong> strictly resupinate poroid
					species were not included. If poroid, the organism had to
					be at least partly reflexed.</p>

					<p><strong>Fifth</strong> - the species must occur in
					Northeast North America. I consulted various references to
					ensure that all the species included in the keys are
					reported to occur in N.E. North America.</p>

					<p><strong>Sixth</strong> - with a few exceptions, slime
					molds were not included. The slime molds I included are
					quite common and fairly conspicuous. Some of these are:
					<em>Fuligo septica</em>, <em>Stemonitis</em> sp., and
					<em>Lycogala epidendrum</em>.</p>
