				<h2>Dr. Gary Emberger</h2>

				<p>I'm a professor of biology at Messiah College where I've
				taught since 1981. My interest in fungi began in a formal
				manner through the introductory mycology course I took at Penn
				State. In spring term, 1974, I took BIOL 418 Mycology with Dr.
				C. Leonard Fergus as the instructor. Dr. Fergus loved the fungi
				and during the course I caught some of his passion for this
				group of organisms. As a result, I'm quite certain I'd still be
				involved with mushrooms as a hobby even if I did not teach
				mycology and biology as a profession. In any event, I combined
				my interest in plants with my new interest in mycology and
				completed graduate work in plant pathology at both Penn State
				and at North Carolina State University. While at NCSU, I took
				PP 625 Advanced Mycology in the fall of 1978, taught by Dr.
				Larry Grand. I still fondly remember staying up all night (the
				only time I ever did this) to complete a collection of fungal
				specimens to be turned in as part of the final grade. I
				reminded him of this at the 2004 NAMA meetings in North
				Carolina! In the plant pathology world of microscopic plant
				pathogens, PP 625 provided an interesting contrast in studying
				some of the macroscopic fungi. Larry is one of the contributing
				photographers for the site.</p>

				<p>In the early years of teaching mycology at Messiah College,
				my students collected a number of specimens which they could
				not identify. Neither could I with any confidence. Eastern Penn
				Mushroomers did not exist and I turned to Dr. Fergus at Penn
				State for help. He graciously offered his assistance. Many of
				the specimens I took to him were wood decay species. As I
				became more proficient at identifying wood decay fungi I
				appreciated their usefulness in teaching a mycology course with
				a field collection requirement. Even when conditions were too
				dry for terrestrial fungi, my students could almost always find
				fungi on logs, stumps, and decaying wood. In addition, many of
				the species are simply beautiful and often spectacular. My
				appreciation for this group of fungi continues to deepen over
				the years.</p>

				<p>Because of my contact with Dr. Fergus, I was aware of his
				book Illustrated Genera of Wood Decay Fungi. I was intrigued by
				the concept of a key to fungi which are restricted to a
				specific habitat, in this case, wood. The idea slowly developed
				in my mind that a web-based identification guide to fungi
				growing on wood would make for an interesting academic project.
				It would be an updated version of the out-of-print Illustrated
				Genera of Wood Decay Fungi. I felt a need existed for this kind
				of guide. And, can there ever be too many field guides to
				fungi? Following a time as department chair, the opportunity
				presented itself to pursue this project. I started work on the
				web site in 2003.</p>
