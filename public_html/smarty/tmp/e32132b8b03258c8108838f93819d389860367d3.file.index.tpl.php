                    <h2>Why Wood Decay Fungi?</h2>

                    <p>Fungi which grow on wood are sometimes called "lignicolous" fungi. But why develop a set of keys
                        limited to fungi utilizing wood as a substrate? After all, being lignicolous does not define a
                        taxonomic category. Lignicolous fungi include
                        <u data-toggle="tooltip" data-placement="top" title="A fungus whose spores develop within asci including
                         molds, mildews, and yeasts">ascomycetes</u> and
                        <u data-toggle="tooltip" data-placement="top" title="A fungus whose spores develop in basidia including
                        the majority of mushrooms and toadstools">basidiomycetes</u> and a large number of classes and orders
                        within each of these groups. Most of these taxa include both lignicolous and terrestrial species.
                        Rather than taxonomy, the keys focus on the biological activity holding this otherwise disparate
                        group of fungi together: their ability to degrade cellulose and lignin, the major components of wood.
                        This ability is often judged negatively on our part. In fact, the introduction to Illustrated Genera of Wood
                        Decay Fungi by Dr. Fergus states that "This manual has therefore been prepared with the hope that it will
                        fill a definite need, that of the general forester to identify decay fungi. It will also provide an
                        illustrated Key for use in a Forest Pathology Laboratory course." Dr. Fergus indicates that the
                        keys in his manual were based on those used by Dr. L.O. Overholts for use in a course in Forest Pathology. Dr.
                        Fergus' book begins to answer the question posed earlier. One reason you might want a set of keys to wood decay fungi
                        is because these fungi cause economic loss. Forest trees and valuable landscape trees can be infected and rotted by
                        these fungi. Knowing the species growing on a tree can help the forester determine the likely extent of loss. Different
                        species are associated with different amounts of decay in the tree. Additionally, some species are restricted to
                        <u data-toggle="tooltip" data-placement="top" title="The soft outer layers of reccently formed wood between
                        the heartwood and the bark">sapwood</u> and will not affect the merchantable volume of heartwood.
                        Some fungi can decay sound wood, others decay only decaying wood and bark.</p>

