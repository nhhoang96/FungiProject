                        <p>But being able to identify lignicolous fungi causing economic loss is only one reason why keys to lignicolous
                            fungi might be useful. In the material below, I briefly describe other important reasons people have to know the
                            species of fungi which are found growing on wood. Wood decay fungi are the preeminant recyclers of wood in
                            ecosystems. Without these fungi, wood would never decay. We would be "up to our eyeballs" in twigs, limbs, and tree
                            trunks. Worse, the valuable nutrients in this wood would be locked up and unavailable for new growth. The species of
                            fungi responsible for decaying the wood of the different species of hardwood and softwood trees is of ecological
                            interest. Wood decay fungi include many sought-after edible species such as
                            <u data-toggle="tooltip" data-placement="top" title="An edible agaric with a soft greyish cap
                            growing in shelving masses on dead wood"><i>Pleurotus ostreatus</i></u>,
                            <u data-toggle="tooltip" data-placement="top" title="A polypore mushroom that grows in clusters
                            at the base of trees, particularly oaks"><i>Grifola frondosa</i></u>, and
                            <u data-toggle="tooltip" data-placement="top" title="A species of bracket fungus found in Europe
                            and North America (grows on trees)"><i>Laetiporus sulphureus</i></u>.</p>

